def gen_pass_hash(passwd):
    salt = "asdf1234ghjk5687"
    db_password = passwd + salt
    hash_pass = hashlib.md5(db_password.encode())
    hash_digest = hash_pass.hexdigest()
    #print(hash_pass)
    #print(hash_digest)
    return hash_digest

####--------------------------------------------------------------------------
####--------------------------------------------------------------------------
def check_pass(passwd, stored_passwd):
    verify = False
    gen_pass = gen_pass_hash(passwd)
    #rint(' -- gen_pass ', gen_pass)
    #print(' -- stored_passwd ', stored_passwd)
    if gen_pass == stored_passwd:
       verify = True
    else:
       verify = False
    return verify

