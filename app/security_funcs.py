string_ascii = 'ABCDEFGHIJKLMNPQRSTUVWYYZ'
digits = '123456789'

def id_generator_4(size=4, chars=string_ascii + digits):
    token_gen = ''.join(random.choice(chars) for _ in range(size))
    return str(token_gen)

def id_generator_8(size=8, chars=string_ascii + digits):
    token_gen = ''.join(random.choice(chars) for _ in range(size))
    return str(token_gen)

